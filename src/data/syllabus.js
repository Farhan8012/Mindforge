// Helper to get chapters for a subject and level
export function getChaptersForLevel(subject, level) {
    const subjectData = syllabusData[subject]
    if (!subjectData) return []

    // Map level IDs to syllabus keys
    const levelMap = {
        'class-9': 'Class 9',
        'class-10': 'Class 10',
        'class-11': 'Class 11',
        'class-12': 'Class 12',
        'jee-main': 'JEE Main',
        'jee-advanced': 'JEE Advanced',
        'neet': 'NEET'
    }

    const levelKey = levelMap[level] || level
    return subjectData[levelKey] || []
}

// Check if a subject has content for a given level
export function hasContentForLevel(subject, level) {
    return getChaptersForLevel(subject, level).length > 0
}
